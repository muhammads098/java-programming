package day12_conditional_statements;

public class IfElseStatements {
    public static void main(String[] args) {
        if(10>5){
            System.out.println("condition is true");
        }else {
            System.out.println("condition is false");
        }

        int count=25;
        if(count>30) {
            System.out.println("Count is more than 30");
        }else{
            System.out.println("count is less than 30");



            int age=11;
            if (age >=18) {
                System.out.println("eligable to vote");
                System.out.println("Age is greater or equal to 18");
            }else{
                System.out.println("not eligable to vote");
                System.out.println("age is less than 18");
            }



        }

    }
}
